use std::collections::*;
use crate::util::*;

fn dfs((r, c): (usize, usize), grid: &[Vec<char>], visited: &mut HashSet<(usize, usize)>) -> (usize, usize) {
    if !visited.insert((r, c)) {
        return (0, 0);
    }

    let rows = grid.len();
    let cols = grid[0].len();
    let mut area = 1;
    let mut perimeter = 4;
    
    for (nr, nc, _) in dirs(r, c, rows, cols) {
        if grid[r][c] == grid[nr][nc] {
            perimeter -= 1;
            let (a, p) = dfs((nr, nc), grid, visited);
            area += a;
            perimeter += p;
        }
    }

    (area, perimeter)
}

/*

area -> # garden plots

perimeter -> sum of the outer sides of the region

region_price = region_area * region_perimeter

total_price = sum(all(regions))
*/
pub fn p1(input: &str) -> usize {
    let mut total = 0;
    let grid = input_to_char_grid(input);
    let rows = grid.len();
    let cols = grid[0].len();
    let mut visited = HashSet::new();

    for r in 0..rows {
        for c in 0..cols {
            let (a, p) = dfs((r, c), &grid, &mut visited);
            total += a * p;
        }
    }

    total
}

