    let mut total = 0;
    let grid = input_to_char_grid(input);
    let rows = grid.len();
    let cols = grid[0].len();
    //                           r      c            r0     r1     c0     c1
    let mut perimeters: HashMap<(usize, usize), Vec<(usize, usize, usize, usize)>> = HashMap::new();

